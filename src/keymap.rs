/// forks
pub(crate) fn get_forks() -> ForksConfig {
    ForksConfig {
        forks: Vec::from_slice(&[
            // . -> :
            fork_alternative_shift(mt!(Dot, RSFT), shifted!(Semicolon)),
            // . -> : (on NUM and others)
            fork_alternative_shift(k!(Dot), shifted!(Semicolon)),
            // , -> ,
            fork_alternative_shift(k!(Comma), k!(Semicolon)),
            // / -> |
            fork_alternative_shift(k!(Slash), shifted!(Backslash)),
            // * -> /
            fork_alternative_shift(k!(KpAsterisk), k!(KpSlash)),
            // + -> -
            fork_alternative_shift(mt!(KpPlus, LSFT), k!(KpMinus)),
            // = -> ^
            fork_alternative_shift(k!(KpEqual), shifted!(Kc6)),
            // { -> }
            fork_alternative_shift(K!("{|}|lвҮ§"), shifted!(RightBracket)),
            // [ -> ]
            fork_alternative_shift(K!("[|]|lвҢғ"), k!(RightBracket)),
            // ( -> )
            fork_alternative_shift(K!("(|)|lвҺҮ"), shifted!(Kc0)),
            // < -> >
            fork_alternative_shift(K!("<|>|lвҢҳ"), shifted!(Dot)),
            // CMD layer shifted alternatives
            // hide app -> minimize window
            fork_alternative_shift(K!("рҹҷҲ"), K!("рҹ—•")),
            // switch app -> switch window
            fork_alternative_shift(K!("вҮЁв§ү"), K!("вҮЁв–ў")),
            // cycle layout -> restart amethyst
            fork_alternative_shift(K!("вҠһвҶ»"), K!("вҠһвҸ»")),
            // close window -> quit app
            fork_alternative_shift(K!("вң•в–ў"), K!("вҸ»")),
        ])
        .expect("error defining forks"),
    }
}

/// Tri-layer: when CRD and CMD layers are both active (holding both right
/// thumbs), activate FUN. `[lower, upper, adjust]` вҖ” adjust turns on while
/// both lower and upper are on.
///
/// NOTE: RMK 0.8.2's tri-layer is **exclusive** вҖ” the adjust layer (FUN)
/// can only be activated via this combination. A direct `LT(FUN, ...)` key
/// would not work because `update_tri_layer` unconditionally overwrites
/// `layer_state[adjust] = lower && upper` after every layer state change,
/// killing any independent activation. This is why FUN has no dedicated
/// momentary key and is reachable only by holding both right thumbs.
pub(crate) fn get_tri_layer() -> [u8; 3] {
    [CRD, CMD, FUN]
}

